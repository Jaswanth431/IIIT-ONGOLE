                    <h1 class="heading-primary text-center">Education at RGUKT</h1>
                    <h3 class="heading-secondary" id="lifeatrgukt">
                        <i class="fa fa-hand-o-right mr-3" aria-hidden="true"></i>
                        Features of Six - Years Integrated Course:
                    </h3>
                    <p>
                        The program offered by the RGUKT consists of a six year integrated course after 10th class
                        examination leading to a B.Tech. degree. The first part of the six-year course is a two-year Pre
                        University Course (equivalent to (AP) Intermediate) followed by a second part of four-year
                        Engineering Course. At present the pre-university part of education offers premature
                        specialization in tracks such as Mathematics, Physics, Chemistry and Life sciences (equivalent
                        to MPC + BiPC). By bringing students in to the University at an earlier stage we hope to impart
                        a broad based education including humanities and liberal arts to students.
                    </p>

                    <h3 class="heading-secondary" id="puc">
                        <i class="fa fa-hand-o-right mr-3" aria-hidden="true"></i>The Pre University Part:
                    </h3>
                    <p>
                        This is equivalent to the two year Intermediate (AP) education. The program provides a
                        qualitative plus two education in itself and further lays a stronger foundation for the
                        technical and professional courses ahead. It has an inbuilt system for remedial education for
                        the students who are relatively weak. In addition, there is a foundation course in IT, preparing
                        students to make full use of the Information Technology and Communication resources provided by
                        the RGUKT in its novel model of teaching learning. There is a permitted exit after
                        Pre-University if the student chooses to go for a career in other professional programs. The
                        program also provides short-term courses in humanities and social sciences so that the students
                        emerge as confident, energetic, enthusiastic youngsters seeking challenges in their university
                        education and life.
                    </p>

                    <h3 class="heading-secondary" id="shopping">
                        <i class="fa fa-hand-o-right mr-3" aria-hidden="true"></i>Degree Programs at IIITs:
                    </h3>
                    <p>
                        Upon completion of 6 years of education, each year consisting of 3 semesters, and the prescribed
                        requirements, the graduates will be awarded a B.Tech. degree in any one of the following
                        engineering disciplines given below
                    </p>
                    <ol>
                        <li>
                            Chemical Engineering
                        </li>
                        <li>
                            Civil Engineering
                        </li>
                        <li>
                            Computer Science and Engineering
                        </li>
                        <li>
                            Electronics & Communications Engineering
                        </li>
                        <li>
                            Materials Science and Metallurgical Engineering
                        </li>
                        <li>
                            Mechanical Engineering
                        </li>
                    </ol>

                    <p>
                        Further, depending on the performance and by earning additional credits, it is possible to get a
                        B.Tech. with two Majors, i.e., one in Computer Science and the other from among Chemical, Civil,
                        Electronics and Communication, Materials Science and Mechanical. Alternatively it is possible to
                        get a degree with one Major and two Minors or one Major and one Minor. One of the two Minors may
                        be from Business Management, Economics Mathematics, Social Sciences and Statistics.
                    </p>
                    <h3 class="heading-secondary" id="extra-activities">
                        <i class="fa fa-hand-o-right mr-3" aria-hidden="true"></i>Employment Prospects:
                    </h3>
                    <p>
                        It is expected that the employment prospects of graduates of RGUKT (IIITs) will be comparable to
                        the other world class universities in India. It is also anticipated that the broad-based
                        education including soft skills will provide a strong foundation for national competitive
                        examinations. Appropriate placement guidance and awareness programs will be conducted to prepare
                        the students for private and public sector opportunities. Students also learn entrepreneurship
                        and other skills that can help them to set out on their own.
                    </p>

                    <h3 class="heading-secondary" id="soft-skills">
                        <i class="fa fa-hand-o-right mr-3" aria-hidden="true"></i>Tuition Fees:
                    </h3>
                    <p>
                        As a government sponsored university, the tuition fees at Rajiv Gandhi University are highly
                        subsidized by the AP government. The annual fee (for AP students) for the first two years (Pre
                        University program) is Rs.36,000/-. This includes tuition, lodging and boarding.
                    </p>

                    <h3 class="heading-secondary" id="healthy">
                        <i class="fa fa-hand-o-right mr-3" aria-hidden="true"></i>Health and Saftey
                    </h3>

                    <p>
                        RGUKT provides its students and staff all the necessary medical facilities. A medical center
                        with the required facilities is under construction and is anticipated to be operational soon.
                        The center will have 30 beds and 3 doctors.
                    </p>

                    <h3 class="heading-secondary" id="healthy">
                        <i class="fa fa-hand-o-right mr-3" aria-hidden="true"></i>Focus on Liberal and Professional
                        Education Including Soft Skills:
                    </h3>

                    <p>
                        The program at RGUKT (IIITs) is built on a new model of liberal professional education creating
                        future leaders with a broad understanding of the world. A structured environment of broad based
                        education including basic skills of communication, teamwork and leadership is provided. It aims
                        at making students well equipped with life skills and confident problem solvers rather than over
                        specialized engineers who just know the theory. It prepares the student to meet the demands of
                        the work place and society as he or she navigates through life. It stresses lifelong learning
                        through learning to learn, and learning to live.
                    </p>
                    <p>
                        The program uses the learning-by-doing paradigm of education using personalized mentors and
                        intelligent tutoring systems. The program adapts self-paced learning with variable duration to
                        reach the mastery level outcome. Highly qualified and meritorious faculty will be participating
                        in teaching, learning and research. Many of them are being drawn from the world class
                        institutions such as IITs.
                    </p>

                    <p>
                        There will be a focus on all-round development with emphasis on personality development and
                        education in Humanities and Social Sciences to make students responsible world citizens. The
                        program is intensive with emphasis on physical training, sports and soft skills. About 30% of
                        time is devoted to soft skills.
                    </p>

                    <h3 class="heading-secondary" id="residential-campus">
                        <i class="fa fa-hand-o-right mr-3" aria-hidden="true"></i>Life in a Residential Campus:
                    </h3>
                    <p>
                        As a fully residential program, all students will stay on the campus. The program is intensive
                        and runs for 3 semesters of 16 weeks of 6 days a week each. Only 6 weeks of vacation is
                        permitted: 1 week in Dasara, 1 week during Sankranthi and four weeks in summer. While the
                        program appears intensive, it is also designed to help absorption by making students apply
                        concepts right-then-and-there in class.
                    </p>
                    <p>
                        All education is mediated through the use of Information and Communication technologies. The
                        academic complex is being connected by high speed LAN which extends to each student's desk and a
                        laptop is provided to each student right from his or her first year. Each class of about 50
                        students will have mentors who will conduct a discourse and discussion around a prerecorded
                        expert lecture. The best part of this learning paradigm is that students will have time in these
                        class sessions to receive the lecture, ask questions, discuss with the help of mentors and apply
                        the concepts to problem solving. A good student would have no homework!
                    </p>
                    <p>
                        To keep up with this program and life in general, time is set apart for physical fitness
                        activities. During the first two years all students would attend physical training and exercise
                        each morning and participate in sports in the evening. The daily academic program will consist
                        of 4 periods of just less than 2 hours duration each from 8 AM to 4PM with a lunch break. During
                        the evening after dinner, there will be programs in soft skills, reading classics, and viewing
                        selected movie programs from 7.30PM to 10.30PM. This will be a more relaxing and a quiet
                        learning time. Students get to explore their talents in art, drama and music as well. Subsequent
                        year will offer greater flexibility in individual daily schedule.
                    </p>

                    <p>
                        Separate and secure hostel facilities will be provided for girls and boys. Food and dining
                        facilities will be according to modern standards.
                    </p>
